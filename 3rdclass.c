#include <stdio.h>
int a,b3,b2,b1,b0;//声明变量
int main(void){
    scanf("%d", &a);
    b3 = a / 1000;//千位
    b2 = (a / 100) % 10;//百位,整除10是因为要去掉千位
    //就比如，按照b2的算法，1234/100=12.34，取整为12，再对10取余，得到2，这才是百位数。
    b1 = (a / 10) % 10;//十位
    b0 = a % 10;//个位
    int c = b0*1000 + b1*100 + b2*10 + b3;//逆序
    printf("逆序: %d\n", c);
    return 0;
}